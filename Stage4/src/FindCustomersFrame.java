    private void searchCustomerByRewardNumber(int rewardNumber) {
        LinkedList<Customer> customerDataBase = Customer.getCustomerDataBase();
        
        String[] columnNames = {"Name", "Phone Number", "Reward Number", "Reward Points"};
        DefaultTableModel model = new DefaultTableModel (columnNames, 0);
        
            boolean customerFound = false;
            for (Customer customer : customerDataBase) {
                if (customer.rewardNumber == rewardNumber) {
                    Object[] rowData = {
                        customer.name,
                        customer.phoneNumber,
                        customer.rewardNumber,
                        customer.rewardPoint
                    };
                    model.addRow(rowData);
                    customerFound = true;
            }
        }
        findCustomersTable.setModel(model);
        
        if (!customerFound) {
            notFoundPane.showMessageDialog(this,
                    "Customer with reward number " + rewardNumber + " not found.",
                    "Customer Not Found",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        
    }

